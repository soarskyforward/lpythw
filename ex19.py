def cheese_and_crackers(cheese_count, cracker_count):
    print(f"There're {cheese_count} cheeses!")
    print(f"And you have {cracker_count} cracker!")

cheese_and_crackers(10,20)
cheese_and_crackers(2*5, 10+10)
amount_cheese = 100
amount_cracker = 200
cheese_and_crackers(amount_cheese,amount_cracker)
