def add(a, b):
    print(f"ADDING {a} + {b}")
    return a + b

def subtract(a, b):
    print(f"SUBTRACT {a} - {b}")
    return a - b

def multiply(a, b):
    print(f"MULTIPLY {a} * {b}")
    return a *b

def divide(a, b):
    print(f"DIVIDING {a} / {b}")
    return a / b

age = add(12, 6)
height = subtract(180, 1)

print(f"AGE: {age}\nHEIGHT: {height}")
